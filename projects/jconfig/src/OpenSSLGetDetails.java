  private boolean valid() {
    if (filename.getText().length() == 0) return false;
    if (country.getText().length() == 0) return false;
    if (state.getText().length() == 0) return false;
    if (city.getText().length() == 0) return false;
    if (company.getText().length() == 0) return false;
    if (department.getText().length() == 0) return false;
    if (domain.getText().length() == 0) return false;
    if (email.getText().length() == 0) return false;
    return true;
  }

  private void setPosition() {
    Rectangle s = GraphicsEnvironment.getLocalGraphicsEnvironment().getMaximumWindowBounds();
    Dimension d = getPreferredSize();
    setLocation(s.width/2 - d.width/2, s.height/2 - (d.height/2));
  }
